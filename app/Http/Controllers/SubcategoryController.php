     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
         $request->validate([
            
            'name'=>'required',
            'category'=>'required',

        ]);

            // Data insert
            $sub=new Subcategory;        
            $sub->name=$request->name;                       
            $sub->category_id=$request->category;  
            $sub->save();

            // Redirect
            return redirect()->route('subcategories.index');  

    }

